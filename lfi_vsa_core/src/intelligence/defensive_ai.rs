/// Runs all defensive AI analyses and aggregates threats.
pub struct DefensiveAIAnalyzer {
    bot_detector: BotDetector,
    /// Total threats detected.
    pub threats_detected: usize,
    /// Recent threat history.
    pub threat_history: Vec<AIThreat>,
}

impl DefensiveAIAnalyzer {
    pub fn new() -> Self {
        debuglog!("DefensiveAIAnalyzer::new: Initializing AI threat detection");
        Self {
            bot_detector: BotDetector::new(),
            threats_detected: 0,
            threat_history: Vec::new(),
        }
    }

    /// Analyze a text input for all relevant threats.
    pub fn analyze_text(&mut self, text: &str) -> Vec<AIThreat> {
        let mut threats = Vec::new();

        // Check for prompt injection (highest priority).
        let injection = PromptInjectionDefender::analyze(text);
        if injection.confidence > 0.3 {
            threats.push(injection);
        }

        // Check for AI-generated text.
        let llm = LLMTextDetector::analyze(text);
        if llm.confidence > 0.4 {
            threats.push(llm);
        }

        // Check for phishing indicators.
        let phishing = PhishingDetector::analyze(text, PhishingContext::Unknown);
        if phishing.confidence > 0.3 {
            threats.push(phishing);
        }

        for t in &threats {
            self.threats_detected += 1;
            self.threat_history.push(t.clone());
        }

        threats
    }

    /// Record a request for bot detection.
    pub fn record_request(&mut self, timestamp_ms: u64) -> AIThreat {
        let threat = self.bot_detector.record_request(timestamp_ms);
        if threat.confidence > 0.3 {
            self.threats_detected += 1;
            self.threat_history.push(threat.clone());
        }
        threat
    }

    /// Overall threat level across recent activity.
    pub fn threat_level(&self) -> ThreatSeverity {
        let recent: Vec<&AIThreat> = self.threat_history.iter()
            .rev()
            .take(20)
            .collect();

        if recent.iter().any(|t| t.severity == ThreatSeverity::Critical) {
            return ThreatSeverity::Critical;
        }
        if recent.iter().filter(|t| t.severity == ThreatSeverity::High).count() >= 2 {
            return ThreatSeverity::High;
        }
        if recent.iter().any(|t| t.severity == ThreatSeverity::High) {
            return ThreatSeverity::Medium;
        }
        if recent.iter().filter(|t| t.severity == ThreatSeverity::Medium).count() >= 3 {
            return ThreatSeverity::Medium;
        }
        ThreatSeverity::Low
    }

    /// Generate a threat report.
    pub fn report(&self) -> String {
        let mut out = format!("=== Defensive AI Threat Report ===\n");
        out.push_str(&format!("Total threats detected: {}\n", self.threats_detected));
        out.push_str(&format!("Current threat level: {:?}\n", self.threat_level()));

        let mut by_category: std::collections::HashMap<String, usize> = std::collections::HashMap::new();
        for t in &self.threat_history {
            *by_category.entry(format!("{:?}", t.category)).or_insert(0) += 1;
        }

        if !by_category.is_empty() {
            out.push_str("\nThreat categories:\n");
            let mut sorted: Vec<_> = by_category.iter().collect();
            sorted.sort_by(|a, b| b.1.cmp(a.1));
            for (cat, count) in sorted {
                out.push_str(&format!("  {:25} {}\n", cat, count));
            }
        }

        out
    }
}

