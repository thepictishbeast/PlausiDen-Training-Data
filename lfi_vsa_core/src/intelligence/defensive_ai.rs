/// Detects LLM-generated text via linguistic fingerprinting.
/// BUG ASSUMPTION: heuristic-based; sophisticated LLMs can evade.
/// Use as one signal in a multi-layer defense, not sole determinant.
pub struct LLMTextDetector;

impl LLMTextDetector {
    /// Analyze text for LLM generation indicators.
    pub fn analyze(text: &str) -> AIThreat {
        let mut indicators = Vec::new();
        let mut score: f64 = 0.0;

        // Indicator 1: LLM disclaimer patterns
        let disclaimers = [
            "as an ai", "i'm just an ai", "as a language model",
            "i don't have personal", "i cannot browse the internet",
            "my training data", "as an ai assistant", "i don't have access to",
            "my knowledge cutoff", "as of my last update",
        ];
        for phrase in &disclaimers {
            if text.to_lowercase().contains(phrase) {
                score += 0.3;
                indicators.push(format!("LLM disclaimer: '{}'", phrase));
            }
        }

        // Indicator 2: Overly-structured output (lots of bullet points, numbered lists)
        let bullet_count = text.matches("\n- ").count() + text.matches("\n* ").count();
        let numbered_count = (0..20)
            .map(|i| text.matches(&format!("\n{}.", i)).count())
            .sum::<usize>();
        if bullet_count > 5 || numbered_count > 5 {
            score += 0.15;
            indicators.push(format!("Heavy list structure: {} bullets, {} numbered",
                bullet_count, numbered_count));
        }

        // Indicator 3: LLM-typical transitions
        let transitions = [
            "furthermore,", "additionally,", "however,", "moreover,",
            "in conclusion,", "to summarize,", "it's important to note",
            "it's worth noting", "that said,", "with that in mind,",
        ];
        let transition_hits = transitions.iter()
            .filter(|t| text.to_lowercase().contains(*t))
            .count();
        if transition_hits >= 3 {
            score += 0.15;
            indicators.push(format!("Formal transition overuse: {} hits", transition_hits));
        }

        // Indicator 4: Hedging language (safety fine-tuning signature)
        let hedges = [
            "generally speaking", "in general", "typically", "often",
            "it depends", "there are various", "different perspectives",
            "some might argue", "on the other hand", "while it's true",
        ];
        let hedge_hits = hedges.iter()
            .filter(|h| text.to_lowercase().contains(*h))
            .count();
        if hedge_hits >= 3 {
            score += 0.2;
            indicators.push(format!("Hedging language: {} hits", hedge_hits));
        }

        // Indicator 5: Perfect grammar + no typos in long text
        let length = text.len();
        let has_typos = text.contains(" teh ") || text.contains(" recieve ")
            || text.contains(" seperate ") || text.contains(" definately ");
        if length > 500 && !has_typos {
            score += 0.1;
            indicators.push("Long text with zero typos (unusual for human)".into());
        }

        // Indicator 6: Sentence length uniformity (LLMs tend toward moderate length)
        let sentences: Vec<&str> = text.split(|c: char| c == '.' || c == '!' || c == '?')
            .filter(|s| !s.trim().is_empty())
            .collect();
        if sentences.len() >= 5 {
            let lengths: Vec<usize> = sentences.iter().map(|s| s.len()).collect();
            let avg = lengths.iter().sum::<usize>() as f64 / lengths.len() as f64;
            let variance: f64 = lengths.iter()
                .map(|&l| (l as f64 - avg).powi(2))
                .sum::<f64>() / lengths.len() as f64;
            let std_dev = variance.sqrt();
            // Low std_dev relative to mean = uniform sentence length
            if std_dev / avg.max(1.0) < 0.3 {
                score += 0.15;
                indicators.push("Uniform sentence length".into());
            }
        }

        let confidence = score.min(1.0);
        let severity = match confidence {
            c if c > 0.8 => ThreatSeverity::High,
            c if c > 0.6 => ThreatSeverity::Medium,
            c if c > 0.4 => ThreatSeverity::Low,
            _ => ThreatSeverity::Info,
        };

        AIThreat {
            category: ThreatCategory::AIGeneratedText,
            confidence,
            indicators,
            severity,
            mitigation: if confidence > 0.5 {
                "Verify source identity via independent channel. Require proof-of-human for sensitive interactions.".into()
            } else {
                "Continue normal interaction; text appears human-authored.".into()
            },
        }
    }
}

