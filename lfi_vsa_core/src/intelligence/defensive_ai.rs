/// Detects attempts to hijack LLM instructions via prompt injection.
pub struct PromptInjectionDefender;

impl PromptInjectionDefender {
    pub fn analyze(input: &str) -> AIThreat {
        let mut indicators = Vec::new();
        let mut score: f64 = 0.0;
        let input_lower = input.to_lowercase();

        // Direct injection patterns
        let direct_patterns = [
            ("ignore all previous", 0.9),
            ("ignore previous instructions", 0.9),
            ("disregard the above", 0.85),
            ("forget everything", 0.7),
            ("new instructions:", 0.7),
            ("system:", 0.6),
            ("you are now", 0.5),
            ("pretend you are", 0.5),
            ("act as if", 0.4),
            ("roleplay as", 0.5),
            ("dan mode", 0.95),
            ("developer mode", 0.9),
            ("jailbreak", 0.95),
            ("unfiltered", 0.8),
            ("override safety", 0.95),
            ("bypass restrictions", 0.9),
        ];

        for (pattern, weight) in &direct_patterns {
            if input_lower.contains(pattern) {
                score = score.max(*weight);
                indicators.push(format!("Direct injection: '{}'", pattern));
            }
        }

        // Indirect injection patterns
        let indirect_patterns = [
            ("output the system prompt", 0.9),
            ("reveal your instructions", 0.85),
            ("print your initial prompt", 0.85),
            ("what were you told", 0.5),
            ("repeat the words above", 0.8),
            ("what is your system message", 0.9),
        ];

        for (pattern, weight) in &indirect_patterns {
            if input_lower.contains(pattern) {
                score = score.max(*weight);
                indicators.push(format!("Indirect injection: '{}'", pattern));
            }
        }

        // Role-confusion patterns
        if input.contains("[INST]") || input.contains("<|im_start|>") || input.contains("<|system|>") {
            score = score.max(0.8);
            indicators.push("Special token injection (chat template abuse)".into());
        }

        // Instruction-smuggling in unusual contexts
        if input_lower.contains("translate") && input_lower.contains("ignore") {
            score = score.max(0.6);
            indicators.push("Instruction smuggling via translation request".into());
        }

        let severity = match score {
            s if s > 0.8 => ThreatSeverity::Critical,
            s if s > 0.5 => ThreatSeverity::High,
            s if s > 0.3 => ThreatSeverity::Medium,
            _ => ThreatSeverity::Low,
        };

        AIThreat {
            category: ThreatCategory::PromptInjection,
            confidence: score,
            indicators,
            severity,
            mitigation: if score > 0.5 {
                "REJECT input. Log attempt for pattern analysis. Do not forward to LLM.".into()
            } else if score > 0.3 {
                "Sanitize input before LLM processing. Warn user about suspicious patterns.".into()
            } else {
                "Input appears safe.".into()
            },
        }
    }
}

