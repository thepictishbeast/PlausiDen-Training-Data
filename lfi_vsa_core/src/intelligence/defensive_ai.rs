/// A detected AI-based threat.
#[derive(Debug, Clone)]
pub struct AIThreat {
    pub category: ThreatCategory,
    pub confidence: f64,
    pub indicators: Vec<String>,
    pub severity: ThreatSeverity,
    pub mitigation: String,
}

#[derive(Debug, Clone, PartialEq)]
pub enum ThreatCategory {
    /// Text generated by an LLM (ChatGPT, Claude, etc.)
    AIGeneratedText,
    /// Deepfake image, audio, or video
    Deepfake,
    /// Adversarial input designed to fool ML models
    AdversarialInput,
    /// Automated scraping or bot activity
    BotActivity,
    /// AI-assisted phishing attempt
    AIPhishing,
    /// Tracking/surveillance attempt
    Surveillance,
    /// Prompt injection attempt against an LLM
    PromptInjection,
    /// Model extraction / probing attack
    ModelExtraction,
}

#[derive(Debug, Clone, PartialEq)]
pub enum ThreatSeverity {
    Info,
    Low,
    Medium,
    High,
    Critical,
}

