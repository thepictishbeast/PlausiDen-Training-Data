#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_profile_empty_knowledge() {
        let engine = SelfImprovementEngine::new();
        let knowledge = KnowledgeEngine::new();
        let profile = engine.profile(&knowledge);

        // KnowledgeEngine seeds 36 concepts, some with domain overlap.
        assert!(profile.overall_score >= 0.0);
        assert!(profile.concepts_known > 0);
    }

    #[test]
    fn test_plan_focuses_on_weak_domains() {
        let engine = SelfImprovementEngine::new();
        let knowledge = KnowledgeEngine::new();
        let profile = engine.profile(&knowledge);
        let plan = engine.plan(&profile);

        assert!(!plan.focus_domains.is_empty(), "Plan should have focus domains");
        assert!(!plan.actions.is_empty(), "Plan should have actions");
        assert!(plan.expected_gain > 0.0, "Plan should expect some gain");
    }

    #[test]
    fn test_run_single_cycle() -> Result<(), HdcError> {
        let mut engine = SelfImprovementEngine::new();
        let mut knowledge = KnowledgeEngine::new();

        let (before, after, plan) = engine.run_cycle(&mut knowledge)?;

        assert!(!plan.actions.is_empty());
        assert!(after.concepts_known >= before.concepts_known,
            "Should learn new concepts: {} → {}", before.concepts_known, after.concepts_known);
        assert_eq!(engine.total_cycles, 1);
        Ok(())
    }

    #[test]
    fn test_run_multiple_cycles_improve() -> Result<(), HdcError> {
        let mut engine = SelfImprovementEngine::new();
        let mut knowledge = KnowledgeEngine::new();

        let profiles = engine.run_n_cycles(3, &mut knowledge)?;

        assert_eq!(profiles.len(), 3);
        // After 3 cycles, should have more concepts than at start.
        let initial_concepts = KnowledgeEngine::new().concept_count();
        let final_concepts = profiles.last().map(|p| p.concepts_known).unwrap_or(0);
        assert!(final_concepts > initial_concepts,
            "Should learn: {} → {}", initial_concepts, final_concepts);
        Ok(())
    }

    #[test]
    fn test_learning_curve_tracking() {
        let mut curve = LearningCurve::new();
        curve.record(0.1);
        curve.record(0.2);
        curve.record(0.3);
        curve.record(0.4);

        assert!(curve.velocity() > 0.0, "Improving curve should have positive velocity");
        assert!(!curve.is_plateaued(), "Improving curve should not be plateaued");
    }

    #[test]
    fn test_plateau_detection() {
        let mut curve = LearningCurve::new();
        // Flat scores → plateau
        for _ in 0..10 {
            curve.record(0.5);
        }

        assert!(curve.is_plateaued(), "Flat scores should detect plateau");
    }

    #[test]
    fn test_progress_report() -> Result<(), HdcError> {
        let mut engine = SelfImprovementEngine::new();
        let mut knowledge = KnowledgeEngine::new();

        let _ = engine.run_cycle(&mut knowledge)?;
        let report = engine.progress_report();

        assert!(report.contains("Self-Improvement Report"));
        assert!(report.contains("Overall:"));
        assert!(report.contains("Math:"));
        assert!(report.contains("Security:"));
        Ok(())
    }

    #[test]
    fn test_improvement_actions_cover_domains() {
        let engine = SelfImprovementEngine::new();
        let knowledge = KnowledgeEngine::new();
        let profile = engine.profile(&knowledge);
        let plan = engine.plan(&profile);

        // Should always include coding practice
        let has_coding = plan.actions.iter().any(|a|
            matches!(a, ImprovementAction::PracticeCoding { .. }));
        assert!(has_coding, "Plan should always include coding practice");
    }

    #[test]
    fn test_cross_training_strategy() {
        let engine = SelfImprovementEngine::new();
        let knowledge = KnowledgeEngine::new();
        let profile = engine.profile(&knowledge);
        let plan = engine.plan(&profile);

        // With fresh knowledge engine, there should be weak domains
        assert!(plan.actions.iter().any(|a|
            matches!(a, ImprovementAction::TrainDomain { .. })),
            "Fresh engine should have TrainDomain actions");
    }

    #[test]
    fn test_domain_velocity_tracking() {
        let mut curve = LearningCurve::new();
        curve.record_domain("math", 0.1);
        curve.record_domain("math", 0.3);
        curve.record_domain("math", 0.5);

        curve.record_domain("security", 0.4);
        curve.record_domain("security", 0.4);
        curve.record_domain("security", 0.4);

        assert!(curve.domain_velocity("math") > 0.0, "Math should be improving");
        assert!(curve.domain_velocity("security").abs() < 0.01, "Security should be flat");

        let plateaued = curve.plateaued_domains();
        assert!(plateaued.contains(&"security".to_string()),
            "Security should be identified as plateaued");
    }
}
