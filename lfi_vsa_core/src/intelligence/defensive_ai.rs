/// Detects AI-assisted phishing attempts.
pub struct PhishingDetector;

impl PhishingDetector {
    pub fn analyze(message: &str, context: PhishingContext) -> AIThreat {
        let mut indicators = Vec::new();
        let mut score: f64 = 0.0;
        let lower = message.to_lowercase();

        // Urgency (classic phishing red flag)
        let urgency_patterns = [
            "urgent", "immediately", "act now", "expires today",
            "your account will be", "24 hours", "suspended",
        ];
        let urgency_hits = urgency_patterns.iter()
            .filter(|p| lower.contains(*p))
            .count();
        if urgency_hits >= 1 {
            score += 0.1 * urgency_hits.min(3) as f64;
            indicators.push(format!("Urgency tactics: {} hits", urgency_hits));
        }

        // Authority impersonation
        let authority_claims = [
            "security team", "it department", "microsoft", "google",
            "apple", "bank of", "irs", "federal", "ceo", "executive",
        ];
        for claim in &authority_claims {
            if lower.contains(claim) {
                score += 0.05;
                indicators.push(format!("Authority claim: '{}'", claim));
                break; // Only count once
            }
        }

        // Suspicious requests
        let suspicious_asks = [
            ("click here", 0.1), ("verify your", 0.15), ("confirm your", 0.15),
            ("update your password", 0.3), ("enter your credentials", 0.4),
            ("gift card", 0.35), ("wire transfer", 0.3), ("bitcoin", 0.25),
            ("social security number", 0.4), ("tax id", 0.3),
        ];
        for (ask, weight) in &suspicious_asks {
            if lower.contains(ask) {
                score += weight;
                indicators.push(format!("Suspicious request: '{}'", ask));
            }
        }

        // Generic greeting (mass phishing tell)
        let generic = [
            "dear customer", "dear user", "valued customer",
            "dear sir/madam", "dear friend",
        ];
        if generic.iter().any(|g| lower.contains(g)) {
            score += 0.1;
            indicators.push("Generic greeting".into());
        }

        // AI-generated signs (combine with LLM detector)
        let llm_threat = LLMTextDetector::analyze(message);
        if llm_threat.confidence > 0.5 {
            score += 0.2;
            indicators.push(format!("AI-generated (conf={:.2})", llm_threat.confidence));
        }

        // Context amplifier: unsolicited message
        if context == PhishingContext::Unsolicited {
            score *= 1.3;
            indicators.push("UNSOLICITED context amplifier".into());
        }

        let confidence = score.min(1.0);
        let severity = match confidence {
            c if c > 0.7 => ThreatSeverity::Critical,
            c if c > 0.4 => ThreatSeverity::High,
            c if c > 0.2 => ThreatSeverity::Medium,
            _ => ThreatSeverity::Low,
        };

        AIThreat {
            category: ThreatCategory::AIPhishing,
            confidence,
            indicators,
            severity,
            mitigation: if confidence > 0.5 {
                "Do not click links. Verify via independent channel. Report to security team.".into()
            } else {
                "Maintain skepticism. Verify sender identity if requesting sensitive actions.".into()
            },
        }
    }
}

#[derive(Debug, Clone, PartialEq)]
pub enum PhishingContext {
    /// User received this unexpectedly
    Unsolicited,
    /// User was expecting contact
    Solicited,
    /// Unknown context
    Unknown,
}

