/// Detects automated bot activity based on behavioral signals.
pub struct BotDetector {
    /// Recent request timestamps (milliseconds).
    request_times: Vec<u64>,
    /// Request count.
    request_count: usize,
}

impl BotDetector {
    pub fn new() -> Self {
        Self {
            request_times: Vec::new(),
            request_count: 0,
        }
    }

    /// Record a request and return threat assessment.
    pub fn record_request(&mut self, timestamp_ms: u64) -> AIThreat {
        self.request_times.push(timestamp_ms);
        self.request_count += 1;

        // Keep only recent (last 60 seconds of requests).
        let cutoff = timestamp_ms.saturating_sub(60_000);
        self.request_times.retain(|&t| t >= cutoff);

        let mut indicators = Vec::new();
        let mut score: f64 = 0.0;

        // Indicator 1: Rate (requests per minute)
        let recent_count = self.request_times.len();
        if recent_count > 60 {
            score += 0.4;
            indicators.push(format!("High request rate: {}/min", recent_count));
        } else if recent_count > 30 {
            score += 0.2;
            indicators.push(format!("Elevated rate: {}/min", recent_count));
        }

        // Indicator 2: Timing regularity (bots have regular intervals)
        if self.request_times.len() >= 5 {
            let deltas: Vec<i64> = self.request_times.windows(2)
                .map(|w| w[1] as i64 - w[0] as i64)
                .collect();
            let mean_delta: f64 = deltas.iter().sum::<i64>() as f64 / deltas.len() as f64;
            let variance: f64 = deltas.iter()
                .map(|&d| (d as f64 - mean_delta).powi(2))
                .sum::<f64>() / deltas.len() as f64;
            let std_dev = variance.sqrt();

            // Bot-like: low variance relative to mean
            if mean_delta > 0.0 && std_dev / mean_delta < 0.1 {
                score += 0.3;
                indicators.push(format!("Regular timing: std_dev/mean={:.3}", std_dev / mean_delta));
            }
        }

        // Indicator 3: Sub-second intervals (nearly impossible for humans)
        if self.request_times.len() >= 2 {
            let smallest_delta = self.request_times.windows(2)
                .map(|w| w[1] - w[0])
                .min()
                .unwrap_or(u64::MAX);
            if smallest_delta < 100 {
                score += 0.3;
                indicators.push(format!("Sub-100ms interval: {}ms", smallest_delta));
            }
        }

        let severity = match score {
            s if s > 0.7 => ThreatSeverity::High,
            s if s > 0.4 => ThreatSeverity::Medium,
            s if s > 0.2 => ThreatSeverity::Low,
            _ => ThreatSeverity::Info,
        };

        AIThreat {
            category: ThreatCategory::BotActivity,
            confidence: score.min(1.0),
            indicators,
            severity,
            mitigation: if score > 0.5 {
                "Apply rate limiting. Require CAPTCHA. Log IP for block consideration.".into()
            } else {
                "Monitor for escalation. Normal user behavior threshold met.".into()
            },
        }
    }

    pub fn reset(&mut self) {
        self.request_times.clear();
        self.request_count = 0;
    }

    pub fn request_count(&self) -> usize {
        self.request_count
    }
}

